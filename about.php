    <p>Forums have a specific set of jargon associated with them; for example, a single conversation is called a "thread", or topic.</p>

    <p>A discussion forum is hierarchical or tree-like in structure; a forum can contain a number of subforums, each of which may have several topics. Within a forum's topic, each new discussion started is called a thread and can be replied to by as many people as they so wish.</p>

    <p>Depending on the forum's settings, users can be anonymous or have to register with the forum and then subsequently log in to post messages. On most forums, users do not have to log in to read existing messages.</p>
    The modern forum originated from bulletin boards and so-called computer conferencing systems, which are a technological evolution of the dial-up bulletin board system.[2][3] From a technological standpoint, forums or boards are web applications that manage user-generated content.[3][4]

    <p>Early Internet forums could be described as a web version of an electronic mailing list or newsgroup (such as those that exist on Usenet), allowing people to post messages and comment on other messages. Later developments emulated the different newsgroups or individual lists, providing more than one forum dedicated to a particular topic.[2]

    <P>Internet forums are prevalent in several developed countries. Japan posts the most,[citation needed] with over two million per day on their largest forum, 2channel. China also has millions of posts on forums such as Tianya Club.

    <p>Some of the first forum systems were the Planet-Forum system, developed at the beginning of the 1970s; the EIES system, first operational in 1976; and the KOM system, first operational in 1977.

